public class IncrementDecrementOperator {
    public static void main(String[] args) {
        int a=10;
        a++;
        //or 
        a=a+1;
        //both same

        int b=20;
        b--;
        //or
        b=b-1;
        //both same 

        int p=7;
        int q=p-- * 5+p*3;
        System.out.println(q);






    }
    
}
